/* ---------- Engine ---------- */
function getLang() {
  return localStorage.getItem('speedbox_lang') || 'fr';
}

function setLang(lang) {
  localStorage.setItem('speedbox_lang', lang);
  applyTranslations();
}

function toggleLang() {
  setLang(getLang() === 'fr' ? 'en' : 'fr');
}

function t(key, params) {
  var lang = getLang();
  var str = (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || (TRANSLATIONS['fr'] && TRANSLATIONS['fr'][key]) || key;
  if (params) {
    Object.keys(params).forEach(function(k) {
      str = str.replace('{' + k + '}', params[k]);
    });
  }
  return str;
}

function applyTranslations() {
  // Static HTML elements with data-i18n
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    var translated = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = translated;
    } else {
      el.textContent = translated;
    }
  });
  // data-i18n-placeholder for inputs that also have visible text
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
  // Update html lang attribute
  document.documentElement.lang = getLang();
  // Update lang toggle buttons
  var lang = getLang();
  var label = lang === 'fr' ? 'EN' : 'FR';
  var btn = document.getElementById('lang-toggle');
  if (btn) btn.textContent = label;
  var btnM = document.getElementById('lang-toggle-mobile');
  if (btnM) btnM.textContent = label;
  // Update clock locale
  if (typeof updateClock === 'function') updateClock();
}

function getLocale() {
  return getLang() === 'fr' ? 'fr-FR' : 'en-GB';
}
